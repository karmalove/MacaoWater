package com.macaowater.app.model;

/**
 * Created by Kevin on 2016/4/26.
 * 自定义控件坐标配置类
 */
public class ViewConfig {
    //自定义控件id
    public int view_Id;
    //自定义控件X坐标
    public int view_X;
    //自定义控件Y坐标
    public int view_Y;

    public int getView_Id() {
        return view_Id;
    }

    public void setView_Id(int view_Id) {
        this.view_Id = view_Id;
    }

    public int getView_X() {
        return view_X;
    }

    public void setView_X(int view_X) {
        this.view_X = view_X;
    }

    public int getView_Y() {
        return view_Y;
    }

    public void setView_Y(int view_Y) {
        this.view_Y = view_Y;
    }


}
